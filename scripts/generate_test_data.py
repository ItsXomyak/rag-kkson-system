def _text_to_html(text: str) -> str:
    """Convert plain text to simple HTML for insert_htmlbox."""
    import html
    escaped = html.escape(text)
    paragraphs = escaped.split("\n\n")
    parts = []
    for p in paragraphs:
        lines = p.split("\n")
        parts.append("<p>" + "<br/>".join(lines) + "</p>")
    return (
        '<div style="font-family: sans-serif; font-size: 10pt; line-height: 1.4;">'
        + "".join(parts)
        + "</div>"
    )


def create_pdf(article: dict, output_dir: Path) -> Path:
    """Create a PDF with proper Cyrillic support via insert_htmlbox."""
    doc = fitz.open()
    rect = fitz.Rect(50, 50, 545, 792)  # A4 margins

    for page_text in article["pages"]:
        page = doc.new_page(width=595, height=842)
        page.insert_htmlbox(rect, _text_to_html(page_text))

    pdf_path = output_dir / article["filename"]
    doc.save(str(pdf_path))
    doc.close()
    return pdf_path


def main() -> None:
    output_dir = Path(sys.argv[1]) if len(sys.argv) > 1 else Path("data/pdfs")
    output_dir.mkdir(parents=True, exist_ok=True)

    print(f"Generating {len(ARTICLES)} test articles into {output_dir}/\n")

    for article in ARTICLES:
        pdf_path = create_pdf(article, output_dir)
        print(f"  OK  {pdf_path.name} ({len(article['pages'])} pages)")

    print(f"\nDone! Run ingestion: python -m scripts.ingest")


if __name__ == "__main__":
    main()
